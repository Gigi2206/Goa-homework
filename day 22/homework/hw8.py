#9) შექმენით string რომელშიც შეინახავთ თქვენს სახელსა და გვარს, შემდეგ ცალ ცალკე გამოიტანეთ ამ სტრინგიდან ჯერ სახელი და შემდეგ გვარი slicing-ის საშვალებით

სრული_სახელი = "გიგი ძირკვაძე"

სახელი = სრული_სახელი[:4] # სახელი იწყება პირველი ინდექსიდან (0) და გრძელდება მე-4 ინდექსამდე (არ შედის)
გვარი = სრული_სახელი[5:] # გვარი იწყება მე-5 ინდექსიდან და გრძელდება სტრიქონის ბოლომდე

print("სახელი:", სახელი)
print("გვარი:", გვარი)