#6) შექმენით სია რომელშიც ჩამოწერეთ რიცხვებს 1-იდან 20-ის ჩათვლით. slicing-ის საშვალებით გამოიტანეთ რიხცები 5-იდან 10-მდე

my_numbers = list(range(1, 21)) # შევქმენით სია 1-დან 20-მდე რიცხვებით
print("თავდაპირველი სია:", my_numbers)

sliced_numbers = my_numbers[4:10] # slicing: ინდექსიდან 4-დან (რადგან 5-ია საჭირო) ინდექსამდე 10-მდე (რადგან 10-ია საჭირო და ბოლო ინდექსი არ შედის)
print("ამოჭრილი რიცხვები 5-დან 10-მდე:", sliced_numbers)