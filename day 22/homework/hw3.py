#4) ახსენით რას ნიშნავს რომ list და string არის დალაგებული მიმდევრობები, კომენტარებით დაწერეთ მათ თვისებებს შორის განსხვავებები. მოიყვანეთ მაგალითები


# დალაგებული მიმდევრობა ნიშნავს, რომ ელემენტებს აქვთ კონკრეტული თანმიმდევრობა
# და ეს თანმიმდევრობა შენარჩუნებულია მთელი სიის ან სტრიქონის განმავლობაში.
# თითოეულ ელემენტს აქვს თავისი უნიკალური ინდექსი (ადგილმდებარეობა) მიმდევრობაში,
# რომელიც იწყება 0-დან პირველი ელემენტისთვის.

# List (სია):
my_list = ["ვაშლი", "ბანანი", "ფორთოხალი"]
print(my_list[0])  # -> "ვაშლი"
print(my_list[1])  # -> "ბანანი"
print(my_list[2])  # -> "ფორთოხალი"

# თვისებები:
# - ელემენტების შეცვლა შესაძლებელია (mutable).
my_list[1] = "მანგო"
print(my_list)  # -> ['ვაშლი', 'მანგო', 'ფორთოხალი']

# - ელემენტების დამატება და წაშლა შესაძლებელია.
my_list.append("კივი")
print(my_list)  # -> ['ვაშლი', 'მანგო', 'ფორთოხალი', 'კივი']
my_list.pop(0)
print(my_list)  # -> ['მანგო', 'ფორთოხალი', 'კივი']

# - შეიძლება შეიცავდეს სხვადასხვა ტიპის მონაცემებს.
mixed_list = [1, "ტექსტი", 3.14, True]
print(mixed_list)  # -> [1, 'ტექსტი', 3.14, True]

# String (სტრიქონი):
my_string = "გამარჯობა"
print(my_string[0])  # -> "გ"
print(my_string[3])  # -> "ა"

# თვისებები:
# - ელემენტების შეცვლა შეუძლებელია (immutable).
#   თქვენ არ შეგიძლიათ პირდაპირ შეცვალოთ სტრიქონის კონკრეტული სიმბოლო ინდექსის გამოყენებით.
#   მაგალითად, my_string[0] = "ს" გამოიწვევს შეცდომას.

# - ელემენტების დამატება და წაშლა პირდაპირ შეუძლებელია.
#   სტრიქონის შესაცვლელად საჭიროა ახალი სტრიქონის შექმნა არსებულის საფუძველზე.
new_string = "ს" + my_string[1:]
print(new_string)  # -> "სამარჯობა"

# - შეიცავს მხოლოდ სიმბოლოებს (char).

# განსხვავებები List-სა და String-ს შორის (კომენტარებში):
# 1. ცვალებადობა (Mutability):
#    - List არის ცვალებადი (mutable), რაც ნიშნავს, რომ მისი ელემენტების შეცვლა,
#      დამატება და წაშლა შესაძლებელია სიის შექმნის შემდეგ.
#    - String არის უცვლელი (immutable), რაც ნიშნავს, რომ მისი შექმნის შემდეგ
#      ცალკეული სიმბოლოების შეცვლა, დამატება ან წაშლა პირდაპირ არ შეიძლება.
#      ნებისმიერი ცვლილება ქმნის ახალ სტრიქონს.

# 2. ელემენტების ტიპი:
#    - List-ს შეუძლია შეიცავდეს სხვადასხვა ტიპის მონაცემებს (მაგალითად, რიცხვები, სტრიქონები, ბულური მნიშვნელობები და სხვა სიები).
#    - String შეიცავს მხოლოდ სიმბოლოებს.

# 3. ოპერაციები:
#    - List-ს აქვს სპეციფიკური მეთოდები ელემენტების დასამატებლად (append, insert),
#      წასაშლელად (remove, pop, clear), დასასორტირებლად (sort), და ა.შ.
#    - String-ს აქვს სპეციფიკური მეთოდები სტრიქონების დასამუშავებლად (მაგალითად, upper, lower, find, replace, split).