# ცვლადები შედარების ოპერაციების შედეგების შესანახად

result1 = 10 > 5  # True (10 მეტია 5-ზე)
result2 = "apple" == "banana"  # False ("apple" არ არის "banana"-ს ტოლი)
result3 = 20 <= 20  # True (20 ნაკლებია ან ტოლია 20-ის)
result4 = 7 != 7.5  # True (7 არ არის 7.5-ის ტოლი)
result5 = True == 1  # True (True Python-ში უდრის 1-ს)

# შედეგების გამოტანა
print(result1)
print(result2)
print(result3)
print(result4)
print(result5)

