# ოპერატორი:
# ოპერატორი არის სიმბოლო ან სიმბოლოების კომბინაცია, რომელიც ასრულებს კონკრეტულ მოქმედებას მონაცემებზე.
# შედარების ოპერატორები (>, <, ==, !=, >=, <=) განსაზღვრავენ, როგორ შევადაროთ ორი მნიშვნელობა.

# ოპერაცია:
# ოპერაცია არის მოქმედება, რომელიც ხორციელდება ოპერატორის გამოყენებით.
# მაგალითად, "10 > 5" არის შედარების ოპერაცია, სადაც > არის ოპერატორი და 10 და 5 არის ოპერანდები.
# ოპერაციის შედეგი არის boolean მნიშვნელობა (True ან False), რომელიც გვიჩვენებს შედარების შედეგს.