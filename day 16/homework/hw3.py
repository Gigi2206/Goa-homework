#4) for ციკლის საშვალებით გამოთვალეთ 20-იდან 100-მდე ყველა ლუწი რიცხვის ჯამი, საბოლოოდ დაბეჭდეთ შედეგი

ჯამი = 0
for რიცხვი in range(20, 101, 2):  # range(20, 101, 2) იწყება 20-დან, მთავრდება 100-ით და ადგენს ნაბიჯს 2-ს (მხოლოდ ლუწი რიცხვები)
    ჯამი += რიცხვი

print("20-იდან 100-მდე ყველა ლუწი რიცხვის ჯამია:", ჯამი)