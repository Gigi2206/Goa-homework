# მომხმარებელს შემოატანინეთ 2 რიცხვი, შემდეგ კი პირველი რიცხვიდან მეორეს ჩათვლით არსებული ყველა რიცხვი დაბეჭდეთ

რიცხვი1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
რიცხვი2 = int(input("შეიყვანეთ მეორე რიცხვი: "))

if რიცხვი1 < რიცხვი2:
    for i in range(რიცხვი1, რიცხვი2 + 1):
        print(i)
elif რიცხვი1 > რიცხვი2:
    for i in range(რიცხვი1, რიცხვი2 - 1, -1):
        print(i)
else:
    print(რიცხვი1)





