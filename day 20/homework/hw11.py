#12) მომხმარებელს შეაყვანინე ინდექსი და ახალი ფერი, შეცვალე ამ ინდექსზე არსებული ფერი სიაში `colors = ["თეთრი", "შავი", "ნარინჯისფერი", "ვარდისფერი"]` და დაბეჭდე განახლებული სია


colors = ["თეთრი", "შავი", "ნარინჯისფერი", "ვარდისფერი"]

while True:
    try:
        index_str = input("გთხოვთ, შეიყვანოთ ინდექსი (0-დან " + str(len(colors) - 1) + "-მდე): ")
        index = int(index_str)
        if 0 <= index < len(colors):
            new_color = input("გთხოვთ, შეიყვანოთ ახალი ფერი: ")
            colors[index] = new_color
            print("განახლებული სია:", colors)
            break
        else:
            print("ინდექსი არასწორია. გთხოვთ, შეიყვანოთ 0-დან " + str(len(colors) - 1) + "-მდე.")
    except ValueError:
        print("გთხოვთ, შეიყვანოთ მთელი რიცხვი ინდექსისთვის.")