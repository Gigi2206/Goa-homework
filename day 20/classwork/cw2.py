#2) მომხმარებელს შემოატანინეთ რიცხვი, შემდეგ თუ ეს რიცხვი დადებითია დაბეჭდეთ ეს და კიდევ შეამოწმეთ ლუწია თუ კენტი, ხოლო თუ არაა დადებითი მხოლოდ დაბეჭდეთ რომ უარყოფითია


# მომხმარებელს შემოაქვს რიცხვი
try:
    რიცხვი = int(input("შეიყვანეთ რიცხვი: "))

    # ვამოწმებთ, არის თუ არა რიცხვი დადებითი
    if რიცხვი > 0:
        print(f"{რიცხვი} დადებითია.")
        # თუ რიცხვი დადებითია, ვამოწმებთ ლუწია თუ კენტი
        if რიცხვი % 2 == 0:
            print(f"{რიცხვი} ლუწია.")
        else:
            print(f"{რიცხვი} კენტია.")
    else:
        print(f"{რიცხვი} უარყოფითია.")

except ValueError:
    print("არასწორი შეყვანა. გთხოვთ შეიყვანოთ მთელი რიცხვი.")