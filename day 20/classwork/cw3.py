#3) შექმენით ბოსტენულის სია და დაბეჭდეთ მთლიანად, შემდეგ კი ამ სიაში ელემენტების პოზიციების საშვალებით დაბეჭდეთ თითეული სიის ელემენტი ცალ-ცალკე

# შევქმნათ ბოსტნეულის სია
vegetables = ["კარტოფილი", "პომიდორი", "კიტრი", "სტაფილო", "ხახვი", "ნიორი"]

# დაბეჭდოთ მთლიანი სია
print("ბოსტნეულის სრული სია:", vegetables)

# დაბეჭდოთ თითოეული ელემენტი ინდექსის მიხედვით
print("\nცალკეული ელემენტები ინდექსის მიხედვით:")
print("ინდექსი 0:", vegetables[0])
print("ინდექსი 1:", vegetables[1])
print("ინდექსი 2:", vegetables[2])
print("ინდექსი 3:", vegetables[3])
print("ინდექსი 4:", vegetables[4])
print("ინდექსი 5:", vegetables[5])

# ალტერნატიული გზა ციკლის გამოყენებით
print("\nცალკეული ელემენტები ციკლის გამოყენებით:")
for i in range(len(vegetables)):
    print(f"ინდექსი {i}: {vegetables[i]}")