#3) შექმენით სიტყვების სია, შემდეგ მის შემობრუნებულ ვერსიას გადაუარეთ for ციკლით, დაბეჭდეთ ყოველი მეორე ელემენტი (რომ გაიგოთ ყოველი მეორე აიღეთ ცვლადი რომელიც თავიდან 0 იქნება, ყოველ გამეორებაზე კი გაზრდით ერთით და შეამოწმეთ ლუწია თუ კენტი)

def დაბეჭდე_შებრუნებული_სია():
  """ქმნის სიტყვების სიას, აბრუნებს მას და ბეჭდავს ყოველ მეორე ელემენტს."""
  სიტყვები = ["ვაშლი", "ბანანი", "ფორთოხალი", "მანგო", "კივი", "ატამი"]
  შებრუნებული_სია = სიტყვები[::-1]

  ინდექსი = 0
  for სიტყვა in შებრუნებული_სია:
    if ინდექსი % 2 == 0:  # ვამოწმებთ ინდექსის ლუწობას
      print(სიტყვა)
    ინდექსი += 1

# ფუნქციის გამოძახება
დაბეჭდე_შებრუნებული_სია()