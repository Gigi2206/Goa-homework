#4) მომხმარებელს შემოატანინეთ სიტყვა და დაბეჭდეთ ის შებრუნებულად

def დაბეჭდე_შებრუნებული_სიტყვა():
  """მომხმარებელს შემოატანინებს სიტყვას და ბეჭდავს მას შებრუნებულად."""
  სიტყვა = input("გთხოვთ, შეიყვანოთ სიტყვა: ")
  შებრუნებული_სიტყვა = სიტყვა[::-1]
  print("შებრუნებული სიტყვა:", შებრუნებული_სიტყვა)

# ფუნქციის გამოძახება
დაბეჭდე_შებრუნებული_სიტყვა()