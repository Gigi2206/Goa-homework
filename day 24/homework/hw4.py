
# შევქმნათ ცვლადი, რომელშიც შევინახავთ 20 სიმბოლოზე მეტ სტრიქონს
ჩემი_სტრიქონი = "ეს არის საკმაოდ გრძელი სტრიქონი ოცზე მეტი სიმბოლოთი"

# 1. მხოლოდ პირველი 5 სიმბოლო
პირველი_ხუთი = ჩემი_სტრიქონი[:5]
print(f"პირველი 5 სიმბოლო: {პირველი_ხუთი}")

# 2. ბოლო 4 სიმბოლო
ბოლო_ოთხი = ჩემი_სტრიქონი[-4:]
print(f"ბოლო 4 სიმბოლო: {ბოლო_ოთხი}")

# 3. შუა ნაწილი მეოთხე სიმბოლოდან მეათე სიმბოლომდე (ინდექსები 3-დან 9-ის ჩათვლით)
შუა_ნაწილი = ჩემი_სტრიქონი[3:10]
print(f"მეოთხედან მეათე სიმბოლომდე: {შუა_ნაწილი}")

# 4. მთელი სტრიქონი შებრუნებულად
შებრუნებული_სტრიქონი = ჩემი_სტრიქონი[::-1]
print(f"შებრუნებული სტრიქონი: {შებრუნებული_სტრიქონი}")

# 5. სტრიქონი ამოკვეთილი ყოველი მეორე სიმბოლოთი
ყოველ_მეორე = ჩემი_სტრიქონი[::2]
print(f"ყოველ მეორე სიმბოლო: {ყოველ_მეორე}")