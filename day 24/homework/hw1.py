#2) მომხმარებელს შემოატანინეთ სიტყვა, შემდეგ if-ით შეამოწმეთ ეს სიტყვა თუ თავისი თავის ტოლია როდესაც შემოაბრუნებთ (slicing-ის დახმარებით), თუ ასეა დაბეჭდეთ რომ განსაკუთრებული (ასეთ სიტყვებს palindrome ჰქვია) სიტყვაა, სხვა შემთხვევაში კი დაბეჭდეთ რომ ჩვეულებრივი სიტყვაა.

def შეამოწმე_პალინდრომი():
  """მომხმარებელს შემოატანინებს სიტყვას და ამოწმებს არის თუ არა ის პალინდრომი."""
  სიტყვა = input("გთხოვთ, შეიყვანოთ სიტყვა: ")
  შებრუნებული_სიტყვა = სიტყვა[::-1]

  if სიტყვა == შებრუნებული_სიტყვა:
    print(f"'{სიტყვა}' განსაკუთრებული სიტყვაა (პალინდრომი).")
  else:
    print(f"'{სიტყვა}' ჩვეულებრივი სიტყვაა.")

# ფუნქციის გამოძახება
შეამოწმე_პალინდრომი()