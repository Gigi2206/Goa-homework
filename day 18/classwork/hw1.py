#2) მოხმარებელს შემოატანინეთ თავისი გამოცდის ქულა, შემდეგ პირობითი განხცადების საშვალებით შეამოწმეთ ეს ქულა მეტია თუ 50-ზე, თუ მეტია დაუბეჭდეთ რომ გამოცდა ჩააბარა

# მომხმარებელს ვთხოვთ შეიყვანოს გამოცდის ქულა
score_str = input("გთხოვთ, შეიყვანოთ თქვენი გამოცდის ქულა: ")

# ვცდილობთ შეყვანილი ტექსტი გადავაქციოთ მთელ რიცხვად
try:
    score = int(score_str)

    # ვამოწმებთ, არის თუ არა ქულა 50-ზე მეტი
    if score > 50:
        print("გილოცავთ! თქვენ წარმატებით ჩააბარეთ გამოცდა.")
    else:
        print("სამწუხაროდ, თქვენ ვერ ჩააბარეთ გამოცდა. სცადეთ ხელახლა.")

except ValueError:
    print("არასწორი შეყვანა. გთხოვთ, შეიყვანოთ მთელი რიცხვი.")