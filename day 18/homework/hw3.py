#4) მომხმარებელს შემოატანინეთ სახელი და თუ ის თქვენს სახელს ემთხვევა დაბეჭდეთ "coincidence"


# მომხმარებლის სახელის შეყვანა და შედარება

მომხმარებლის_სახელი = input("გთხოვთ, შეიყვანოთ თქვენი სახელი: ")
ჩემი_სახელი = "გიორგი"

if მომხმარებლის_სახელი.lower() == ჩემი_სახელი.lower():
  print("თქვენი სახელი ემთხვევა ჩემს სახელს!")
else:
  print("თქვენი სახელი არ ემთხვევა ჩემს სახელს.")
