# while ციკლის გამოყენებით 0-დან 20-მდე რიცხვების შემოწმება

რიცხვი = 0
while რიცხვი <= 20:
  if რიცხვი % 2 == 0:
    print(f"{რიცხვი} არის ლუწი")
  else:
    print(f"{რიცხვი} არის კენტი")
  რიცხვი += 1